    private void jButtonProjetosRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonProjetosRemoverMouseClicked
        if (jTableProjetos.getSelectedRow() != -1) {
            DefaultTableModel dtmTabela = (DefaultTableModel) jTableProjetos.getModel();
            int index = jTableProjetos.getSelectedRow();
            dtmTabela.removeRow(index);
            Projeto.remover(index, Projeto.ler());
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para remover.");
        }
    }//GEN-LAST:event_jButtonProjetosRemoverMouseClicked

    private void jButtonProjetosEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonProjetosEditarMouseClicked
        if (jTableProjetos.getSelectedRow() != -1) {
            EditarProjeto ep = new EditarProjeto(jTableProjetos.getSelectedRow(), Projeto.ler());
            ep.setVisible(true);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um item para editar.");
        }
    }//GEN-LAST:event_jButtonProjetosEditarMouseClicked

