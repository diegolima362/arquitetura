    private void jButtonFuncionarioSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonFuncionarioSalvarMouseClicked
        if (jTextFieldFuncionarioNome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioTelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioPais.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioEstado.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioBairro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioRua.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioNumero.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioSalario1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else if (jTextFieldFuncionarioProjetoAlocado.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos");
            limparMenuFuncionario();
        } else {

            funcionario.setNome(jTextFieldFuncionarioNome.getText());
            funcionario.setCodigo(Integer.parseInt(jTextFieldFuncionarioCodigo.getText()));
            funcionario.setTelefone(Integer.parseInt(jTextFieldFuncionarioTelefone.getText()));
            funcionario.setSalario(Float.parseFloat(jTextFieldFuncionarioSalario1.getText()));
            funcionario.setProjetoAlocado(jTextFieldFuncionarioProjetoAlocado.getText());
            funcionario.setEndereco(new Endereco(
                    jTextFieldFuncionarioPais.getText(),
                    jTextFieldFuncionarioEstado.getText(),
                    jTextFieldFuncionarioCidade.getText(),
                    jTextFieldFuncionarioBairro.getText(),
                    jTextFieldFuncionarioRua.getText(),
                    Integer.parseInt(jTextFieldFuncionarioNumero.getText())
            ));

            funcionarios.remove(index);
            funcionarios.add(index, funcionario);
            Funcionario.escrever(funcionarios);

            JOptionPane.showMessageDialog(null, "Salva com sucesso.");
            dispose();

        }
    }//GEN-LAST:event_jButtonFuncionarioSalvarMouseClicked
